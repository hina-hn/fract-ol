#include "fractol.h"

double	map(double unscaled_num, double new_min, double new_max, t_range old)
{
	return ((new_max - new_min) * (unscaled_num - old.old_min) / (old.old_max
			- old.old_min) + new_min);
}

t_complex	sum_complex(t_complex z1, t_complex z2)
{
	t_complex	result;

	result.x = z1.x + z2.x;
	result.y = z1.y + z2.y;
	return (result);
}

t_complex	square_complex(t_complex z)
{
	t_complex	result;

	result.x = (z.x * z.x) - (z.y * z.y);
	result.y = 2 * z.x * z.y;
	return (result);
}
